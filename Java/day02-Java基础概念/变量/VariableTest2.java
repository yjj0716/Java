public class VariableTest2{
	//主入口
	public static void main(String[] args){
		//定义变量记录名字
		String name = "夜金甲";
		//定义变量记录年龄
		int age = 22;
		//定义变量记录性别
		char gender = '男';
		//定义变量记录身高
		double height = 168.5;
		//定义变量记录是否单身
		//true 单身 false 不是单身
		boolean flag = true;
		
		//输出变量的值
		System.out.println(name);
		System.out.println(age);
		System.out.println(gender);
		System.out.println(height);
		System.out.println(flag);
	}
}